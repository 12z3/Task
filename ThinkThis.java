        int[] a = {1, 15, 12, 3, 5, 7, 4, 9};

        for (int i = 0; i < a.length; i++) {
            for (int j = 0; j < a.length - i - 1; j++) {
                if (a[j] > a[j + 1]) {
                    int tmp = a[j];
                    a[j] = a[j + 1];
                    a[j + 1] = tmp;
                    System.out.println("i = " + i + ", j = " + j + ":  " + Arrays.toString(a));
                }
            }
        }


